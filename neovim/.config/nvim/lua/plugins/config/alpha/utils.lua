local M = {}

M.button = function(shortcut, txt, keybind)
  local shortcut_ = shortcut:gsub('%s', ''):gsub('SPC', '<leader>')

  local opts = {
    position = 'center',
    text = txt,
    shortcut = shortcut,
    cursor = 4,
    width = 50,
    align_shortcut = 'right',
    hl = 'AlphaButtons',
    hl_shortcut = 'AlphaShortcut',
  }

  if keybind then
    opts.keymap = { 'n', shortcut_, keybind, { noremap = true, silent = true } }
  end

  return {
    type = 'button',
    val = txt,
    on_press = function()
      local key = vim.api.nvim_replace_termcodes(shortcut_, true, false, true)
      vim.api.nvim_feedkeys(key, 'normal', false)
    end,
    opts = opts,
  }
end

